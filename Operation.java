public class Operation <T>{
    public static Double calc(Double num1, Double num2, char operation){
        Double result;
        switch (operation){
            case '+':
                result = num1+num2;
                break;
            case '-':
                result = num1-num2;
                break;
            case '*':
                result = num1*num2;
                break;
            case '/':
                result = num1/num2;
                break;
            default:
                System.out.println("Операция не распознана. Повторите ввод.");
                result = calc(num1, num2, Input.getOperation());//рекурсия
        }
        return result;
    }
}
